public class AngleAcceleration {

	private int angle;
	private float acceleration;
	
	public AngleAcceleration(int angle, float acceleration)
	{
		this.angle = angle;
		this.acceleration = acceleration;
	}
	
	public int angle()
	{
		return angle;
	}
	public float acceleration()
	{
		return acceleration;
	}
}
